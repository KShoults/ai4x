namespace Engine
{
    /// <summary>
    /// Enum <c>DistrictType</c> represent the types of districts that can
    /// be built at a colony.
    /// </summary>
    public enum DistrictType
    {
        /// <summary>
        /// a mining district that collects minerals.
        /// </summary>
        Mining,
        /// <summary>
        /// a foundry district that converts minerals to refined metals.
        /// </summary>
        Foundry
    }
}
