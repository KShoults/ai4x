namespace Engine
{
    /// <summary>
    /// Enum <c>StellarClass</c> refers to the spectral class of a star.
    /// </summary>
    public enum StellarClass
    {
        /// <summary>
        /// Refers to a blue O class star.
        /// </summary>
        O,
        /// <summary>
        /// Refers to a blue-white B class star.
        /// </summary>
        B,
        /// <summary>
        /// Refers to a white A class star.
        /// </summary>
        A,
        /// <summary>
        /// Refers to a yellow-white F class star.
        /// </summary>
        F,
        /// <summary>
        /// Refers to a yellow G class star.
        /// </summary>
        G,
        /// <summary>
        /// Refers to a light-orange k class star.
        /// </summary>
        K,
        /// <summary>
        /// Refers to an orange-red M class star.
        /// </summary>
        M
    }
}
